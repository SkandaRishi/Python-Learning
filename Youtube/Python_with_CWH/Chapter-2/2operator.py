# operator for addition +
print(5 + 5)

# difference between two number -
print(5 - 9)

# Product of two number *
print(5 * 9)

# Division of two number /
print(15 / 6)

# Modulus of two number, ye remider deta hai
print(15 % 6)

# Floor division, ishme approx value liya jata hai
print(15 // 6)

# Exponential ye samjho jaise 5**3 hai mean 5ko 5 se 3 bar multiply karega wahi 4 hota toh 5 ko 5 se 4 bar
print(5**3)
